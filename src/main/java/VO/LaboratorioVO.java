public class LaboratorioVO {
    
    public Integer Id_Laboratorio;
    public String Nombre;
    public String Direccion;
    public String E_mail;
    public Integer Telefono;
    public Integer Id_Localidad;
    public Integer Id_Motivo;
    public Integer Id_Estado;

    /**
     * @return the Id_Laboratorio
     */
    public Integer getId_Laboratorio() {
        return Id_Laboratorio;
    }

    /**
     * @param Id_Laboratorio the Id_Laboratorio to set
     */
    public void setId_Laboratorio(Integer Id_Laboratorio) {
        this.Id_Laboratorio = Id_Laboratorio;
    }

    /**
     * @return the Nombre
     */
    public String getNombre() {
        return Nombre;
    }

    /**
     * @param Nombre the Nombre to set
     */
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    /**
     * @return the Direccion
     */
    public String getDireccion() {
        return Direccion;
    }

    /**
     * @param Direccion the Direccion to set
     */
    public void setDireccion(String Direccion) {
        this.Direccion = Direccion;
    }

    /**
     * @return the E_mail
     */
    public String getE_mail() {
        return E_mail;
    }

    /**
     * @param E_mail the E_mail to set
     */
    public void setE_mail(String E_mail) {
        this.E_mail = E_mail;
    }

    /**
     * @return the Telefono
     */
    public Integer getTelefono() {
        return Telefono;
    }

    /**
     * @param Telefono the Telefono to set
     */
    public void setTelefono(Integer Telefono) {
        this.Telefono = Telefono;
    }

    /**
     * @return the Id_Localidad
     */
    public Integer getId_Localidad() {
        return Id_Localidad;
    }

    /**
     * @param Id_Localidad the Id_Localidad to set
     */
    public void setId_Localidad(Integer Id_Localidad) {
        this.Id_Localidad = Id_Localidad;
    }

    /**
     * @return the Id_Motivo
     */
    public Integer getId_Motivo() {
        return Id_Motivo;
    }

    /**
     * @param Id_Motivo the Id_Motivo to set
     */
    public void setId_Motivo(Integer Id_Motivo) {
        this.Id_Motivo = Id_Motivo;
    }

    /**
     * @return the Id_Estado
     */
    public Integer getId_Estado() {
        return Id_Estado;
    }

    /**
     * @param Id_Estado the Id_Estado to set
     */
    public void setId_Estado(Integer Id_Estado) {
        this.Id_Estado = Id_Estado;
    }
    
    
}
